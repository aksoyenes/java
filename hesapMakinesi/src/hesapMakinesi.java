public class hesapMakinesi {
    public int topla(int sayı1,int sayı2){
        return sayı1+sayı2;
    }
    public int çıkar(int sayı1,int sayı2){
        return sayı1-sayı2;
    }
    public int böl(int sayı1,int sayı2){
        return sayı1/sayı2;
    }
    public int çarp(int sayı1,int sayı2){
        return sayı1*sayı2;
    }
}
